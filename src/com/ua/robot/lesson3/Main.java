package com.ua.robot.lesson3;

public class Main {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        System.out.println("The current value of the variable a = " + a);
        System.out.println("The current value of the variable b = " + b);
        System.out.println("a + b = ");
        System.out.println(a + b);
        System.out.println("a - b = ");
        System.out.println(a - b);
        System.out.println("a / b = ");
        System.out.println(a / b);
        System.out.println("a * b = ");
        System.out.println(a * b);
    }
}
